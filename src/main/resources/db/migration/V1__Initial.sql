CREATE TABLE MENTORAI_USER (
    ID       NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NAME     VARCHAR2(150) NOT NULL,
    EMAIL    VARCHAR2(150) NOT NULL UNIQUE,
    PASSWORD VARCHAR2(200) NOT NULL
);

CREATE TABLE MENTORAI_SKILL (
    ID          NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NAME        VARCHAR2(150) NOT NULL UNIQUE,
    DESCRIPTION VARCHAR2(500)
);

CREATE TABLE MENTORAI_COURSE (
     ID             NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
     TITLE          VARCHAR2(200) NOT NULL,
     DESCRIPTION    VARCHAR2(1000),
     WORKLOAD_HOURS NUMBER(5),
     PROVIDER       VARCHAR2(150)
);

CREATE TABLE MENTORAI_COURSE_SKILL (
    COURSE_ID NUMBER NOT NULL,
    SKILL_ID  NUMBER NOT NULL,
    CONSTRAINT PK_MENTORAI_COURSE_SKILL PRIMARY KEY (COURSE_ID, SKILL_ID),
    CONSTRAINT FK_MENTORAI_COURSE_SKILL_COURSE FOREIGN KEY (COURSE_ID)
       REFERENCES MENTORAI_COURSE(ID),
    CONSTRAINT FK_MENTORAI_COURSE_SKILL_SKILL FOREIGN KEY (SKILL_ID)
       REFERENCES MENTORAI_SKILL(ID)
);
