CREATE TABLE MENTORAI_USER_SKILL (
    ID       NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    USER_ID  NUMBER NOT NULL,
    SKILL_ID NUMBER NOT NULL,

    CONSTRAINT FK_MENTORAI_USER_SKILL_USER FOREIGN KEY (USER_ID)
     REFERENCES MENTORAI_USER(ID),

    CONSTRAINT FK_MENTORAI_USER_SKILL_SKILL FOREIGN KEY (SKILL_ID)
     REFERENCES MENTORAI_SKILL(ID)
);


CREATE TABLE MENTORAI_ENROLLMENT (
    ID                  NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    USER_ID             NUMBER NOT NULL,
    COURSE_ID           NUMBER NOT NULL,
    STATUS              VARCHAR2(50),

    CONSTRAINT FK_MENTORAI_ENROLLMENT_USER FOREIGN KEY (USER_ID)
     REFERENCES MENTORAI_USER(ID),

    CONSTRAINT FK_MENTORAI_ENROLLMENT_COURSE FOREIGN KEY (COURSE_ID)
     REFERENCES MENTORAI_COURSE(ID)
);
